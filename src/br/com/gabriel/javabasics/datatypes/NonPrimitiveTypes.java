package javabasics.datatypes;

public class NonPrimitiveTypes {
    public static void main(String[] args) {

        String name = "Gabriel Lacerda";
        String nameUpperCase = name.toUpperCase();
        System.out.println("Original name: " + name);
        System.out.println("Uppercase name: " + nameUpperCase);

        String city = "BELO HORIZONTE";
        String cityLowerCase = city.toLowerCase();
        System.out.println("City in lowercase: " + cityLowerCase);
    }
}
